public class Entidad {

    private String correoContaco;
    private String direccion;
    private String idNit;
    private String nombre;
    private String nombreContacto;
    private String telefonoContacto;

    public String getCorreoContaco() {
        return correoContaco;
    }

    public void setCorreoContaco(String correoContaco) {
        this.correoContaco = correoContaco;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getIdNit() {
        return idNit;
    }

    public void setIdNit(String idNit) {
        this.idNit = idNit;
    }

    public String getNombreContacto() {
        return nombreContacto;
    }

    public void setNombreContacto(String nombreContacto) {
        this.nombreContacto = nombreContacto;
    }

    public String getTelefonoContacto() {
        return telefonoContacto;
    }

    public void setTelefonoContacto(String telefonoContacto) {
        this.telefonoContacto = telefonoContacto;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
